public class PrimitiveVar {
    
    public static void main(String args[]){
        int num = 233;
        byte num1 = 127;
        boolean b = true;
        short num2 = 949;

        long num3 = 3029l;

        char c = 'k';
        float num4 = 5.6f;
        double num5 = 5.6;
        c++;
        System.out.println(c); // value will be l
    }
}
